/*
 * Number Bean to hold and pass values
 */

package converter;

public class NumberBean
{
	//variable to store octal number values and wrapped aroung by getter-setters
	public int octal;

	//getter for octal number
	public int getOctal()
	{
		return octal;
	}

	//setter for octal number
	public void setOctal(int octal)
	{
		this.octal = octal;
	}
	
}
